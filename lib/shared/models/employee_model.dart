class Employee {
  final String id;
  final String name;
  final String role;
  final String department;
  final double salary;
  final bool isPresent;
  final String imageUrl;

  Employee({
    required this.id,
    required this.name,
    required this.role,
    required this.department,
    required this.salary,
    required this.isPresent,
    required this.imageUrl,
  });
}
